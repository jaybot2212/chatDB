CREATE TABLE Teams (
    TeamID INT PRIMARY KEY,
    TeamName VARCHAR(100),
    CEO VARCHAR(100),
    Owner VARCHAR(100),
    Location VARCHAR(100),
    Stadium VARCHAR(100),
    FoundedYear INT,
    NetWorth_USD FLOAT
);

CREATE TABLE Players (
    PlayerID INT PRIMARY KEY,
    FirstName VARCHAR(100),
    LastName VARCHAR(100),
    Position VARCHAR(20),
    TeamID INT,
    Height_cm INT,
    Weight_kg INT,
    Birthdate DATE,
    Nationality VARCHAR(100),
    PointsPerGame FLOAT,
    ReboundsPerGame FLOAT,
    AssistsPerGame FLOAT,
    StealsPerGame FLOAT,
    BlocksPerGame FLOAT,
    FieldGoalPercentage FLOAT,
    ThreePointPercentage FLOAT,
    FreeThrowPercentage FLOAT,
    NetWorth_USD FLOAT,
    FOREIGN KEY (TeamID) REFERENCES Teams(TeamID)
);

CREATE TABLE Games (
    GameID INT PRIMARY KEY,
    HomeTeamID INT,
    GuestTeamID INT,
    Time DATETIME,
    Score VARCHAR(50),
    Round VARCHAR(50),
    GameNumber VARCHAR(50),
    FOREIGN KEY (HomeTeamID) REFERENCES Teams(TeamID),
    FOREIGN KEY (GuestTeamID) REFERENCES Teams(TeamID)
);

